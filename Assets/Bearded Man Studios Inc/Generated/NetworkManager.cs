						if (CardDealerNetworkObject.Length > 0 && CardDealerNetworkObject[obj.CreateCode] != null)
						{
							var go = Instantiate(CardDealerNetworkObject[obj.CreateCode]);
							newObj = go.GetComponent<CardDealerBehavior>();
						}
					}

					if (newObj == null)
						return;
						
